public class App {
    public static void main(String[] args) {
        // For loop
        for (int i = 0; i < 5; i++) {
            System.out.println("Executin loop " + i);
        }

        // while loop
        int number = 0;
        while (number < 5) {
            System.out.println("Executing loop " + number);
            number++;
        }
    }
}